// Function to generate HTML for pet cards
function generatePetCards() {
    var petCardsHtml = '';
    var categories = [];

    pets.forEach(function(pet) {
        if (!categories.includes(pet.category)) {
            categories.push(pet.category);
            petCardsHtml += `<div class="category">${pet.category}</div>`;
        }

        petCardsHtml += `
            <div class="pet-card">
                <img src="${pet.image}" alt="${pet.name}">
                <h3>${pet.name}</h3>
                <p>${pet.description}</p>
                <p>Price: ${pet.price}</p>
                <button class="add-to-cart-btn">Add to Cart</button>
            </div>
        `;
    });

    document.querySelector('.pet-cards').innerHTML = petCardsHtml;
}

